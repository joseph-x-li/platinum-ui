//! platinum-ui — a Mac OS Platinum-styled **design system** for Leptos.
//!
//! This is an entire design system, not a bag of drop-in widgets: it assumes
//! it owns the whole page. Mounting it installs a global reset (Tailwind-style
//! preflight), element-level rules (buttons, inputs, headings, scrollbars),
//! and the token palette — every element on the page renders Platinum. It is
//! deliberately **all or nothing**: don't embed these components in a page
//! whose styling you don't want rewritten, and don't expect an opt-out — apps
//! either live on this system or don't use it.
//!
//! There is exactly ONE way to consume it — no Tailwind toolchain, no Node:
//! mount [`PlatinumStyles`] once near your app root. It injects
//! [`PLATINUM_CSS`] — preflight, then the hand-written skin, then utility CSS
//! for every class the components emit, generated at compile time by
//! encre-css in this crate's build.rs. Serve the `fonts/` woff2 files at your
//! web root (or accept the fallback fonts). For your app's OWN utility
//! classes, run encre-css over your sources in your app's build.rs and
//! inject the output as a second `<style>` — see `showcase/build.rs` for the
//! canonical setup.

