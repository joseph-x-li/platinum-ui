/// The complete Platinum stylesheet, in cascade order:
///
/// 1. **Preflight** — the Tailwind-v4-style global reset (generated by
///    build.rs via encre-css), including the `--en-*` variable defaults that
///    composed utilities resolve against. Shipping it is what makes this a
///    design system rather than a widget library: components are written
///    against this flat baseline, never against browser UA defaults (see the
///    "preflight gap" notes in CLAUDE.md).
/// 2. **`platinum.css`** — tokens + the hand-written skin; comes after the
///    reset so its element rules win.
/// 3. **Utility CSS** for every Tailwind-compatible class the components
///    emit (generated by build.rs via encre-css) — last, so utilities win
///    over both. No Node toolchain and no consumer-side scanning involved.
pub const PLATINUM_CSS: &str = concat!(
    "/* ---- Preflight (generated by build.rs via encre-css) ---- */\n",
    include_str!(concat!(env!("OUT_DIR"), "/platinum-preflight.css")),
    "\n/* ---- Tokens + skin (platinum.css) ---- */\n",
    include_str!("../platinum.css"),
    "\n/* ---- Component utility classes (generated by build.rs via encre-css) ---- */\n",
    include_str!(concat!(env!("OUT_DIR"), "/platinum-utilities.css")),
);

/// Injects [`PLATINUM_CSS`] as a `<style>` element — the whole design system
/// in one component. Mount it once, before/above everything it styles:
///
/// ```ignore
/// view! {
///     <PlatinumStyles/>
///     <App/>
/// }
/// ```
#[component]
pub fn PlatinumStyles() -> impl IntoView {
    view! { <style>{PLATINUM_CSS}</style> }
}

/// Join a component's base utility classes with caller-supplied ones — plain
/// concatenation, deliberately NOT tw_merge: its class-name parsing mis-reads
/// design-system classes (`pl-*` → Tailwind padding-left) and silently deletes
/// them (see ui_button.rs). Base class lists are written to compose without
/// utility conflicts instead of relying on a merger to resolve them.
pub(crate) fn classes(base: &str, extra: &str) -> String {
    if extra.is_empty() {
        base.to_string()
    } else if base.is_empty() {
        extra.to_string()
    } else {
        format!("{base} {extra}")
    }
}
